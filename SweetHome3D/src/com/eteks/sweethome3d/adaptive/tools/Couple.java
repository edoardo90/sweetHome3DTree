package com.eteks.sweethome3d.adaptive.tools;

public class Couple<T, S> {
    private T first;
    private S second;
    public T getFirst() {
      return first;
    }
    public void setFirst(T first) {
      this.first = first;
    }
    public S getSecond() {
      return second;
    }
    public void setSecond(S second) {
      this.second = second;
    }
  
}
